package model;

/**
 *
 * @author Admin
 */
public class Location {
    private int id;
    private String area;
    private String status;
    private String type;
    

    public Location() {
    }

    public Location(int id, String area, String status, String type) {
        this.id = id;
        this.area = area;
        this.status = status;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    
}
